import math
import random

print(math.pow(10 , 2)) #Aqui se pide la funcion pow de math que eleva el primer valor y el segundo es el que lo elevas
print(math.sqrt(16)) #Aqui se pide la funcion sqrt de math que saca la raiz cuadrada al valor ingresado en float
print(math.isqrt(64)) #Aqui se pide la funcion sqrt de math que saca la raiz cuadrada al valor ingresado en int
print(math.sin(99)) #Funciones trigonometricas seno
print(math.cos(99)) #Funciones trigonometricas coseno
print(math.tan(99)) #Funciones trigonometricas tangente
print(math.factorial(10)) #Factorial del num ingresado

print(random.randint(1, 100))